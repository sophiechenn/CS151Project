
public class Room {
	private int roomNumber;
	private boolean available;
	private int rate;
	
	public boolean isAvailable() {
		return available;
	}
	public void setAvailable(boolean available) {
		this.available = available;
	}
	public int getRoomNumber() {
		return roomNumber;
	}
	
	public int getRate()
	{
		return rate;
	}
	
}
